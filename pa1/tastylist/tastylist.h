int delete(int num)
{
    	//list is empty
    	if(head == NULL){
		return 0;
    	}
	// temp holds address of pointer to node
	// *temp is a ponter to a node (address of node)
	struct node **temp;
	temp = &head;
	//walk the list
	while((*temp)->data != num){
		//if num not in list
		if((*temp)->next == NULL){
			return 0;
		}
		temp = &(*temp)->next; 
	}
	
	struct node *old; //holds address of node to free
	old = *temp;	// set old to *temp which we want to delete
	*temp = (*temp)->next; // set *temp to next value
	free(old); // free address we want to delete
    
    return 1;
}
 
 
